const base64ToUint8 = (base64) => Uint8Array.from(atob(base64), c => c.charCodeAt(0));

async function deriveKeyFromPassword(password) {
    if (!password) return null;
    try {
        const encoder = new TextEncoder();
        const keyMaterial = await crypto.subtle.importKey(
            "raw",
            encoder.encode(password), {
                name: "PBKDF2"
            },
            false, ["deriveKey"]
        );
        
        return await crypto.subtle.deriveKey({
                name: "PBKDF2",
                salt: encoder.encode('OdesofLang:Zibai'),
                iterations: 100000,
                hash: "SHA-256"
            },
            keyMaterial, {
                name: "AES-GCM",
                length: 256
            },
            false, ["decrypt"]
        );
    } catch (e) {
        return null;
    }
}

async function decryptData(ciphertextBase64, key) {
    if (!key || !ciphertextBase64) return null;
    try {
        const data = base64ToUint8(ciphertextBase64);
        const iv = data.slice(0, 12);
        const encrypted = data.slice(12);
        
        const decrypted = await crypto.subtle.decrypt({
                name: "AES-GCM",
                iv: iv
            },
            key,
            encrypted
        );
        return new TextDecoder().decode(decrypted);
    } catch (err) {
        return null;
    }
}

